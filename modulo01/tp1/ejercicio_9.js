let vocales = ["A","E","I","O","U"]  //Declaro el array.

for (i=0;i<=4;i++) {     // Con la funcion for se genera un bucle que recorre en este caso desde la pos 0 hasta la 4 del array.
    document.write(vocales [i])  //Escribe en la pagina la posicion del array dependiendo de que posicion se este ejecutando el bucle.
    document.write("</br>") //Salto de linea.
}